package com.pawan.choure.basic;

public class IntegertoBinaryAndViceVersa {

	public static void main(String[] args) {
	
		//Convert Integer to Binary Equivalent
		System.out.println("Integer Value : " + 100 + " Binary Value : "+Integer.toString(100, 2));
		
		//Converts Binary to Integer Equivalent
		System.out.println("Binary Value : " + "1100100" + " Integer Value : "+Integer.parseInt("1100100", 2));
		
		

	}

}
